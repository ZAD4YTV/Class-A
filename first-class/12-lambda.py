# A lambda function is a small anonymous function.

# A lambda function can take any number of arguments, but can only have one expression.

# For example:

x = lambda a: a + 10
print(x(5)) # This will return 15


def X(a): return a + 10
print(X(5)) # And this, also will return 15



# I can use more than 1 argument, for example:
y = lambda a, b: a * b
print(y(5, 6))


def Y(a, b): return a * b
print(Y(5, 6))
